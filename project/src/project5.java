    private void selectImage(JButton button) {
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setDialogTitle("Choose an Image File");

        int returnValue = fileChooser.showOpenDialog(this);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();

            // Set the image as an icon on the button
            try {
                ImageIcon imageIcon = new ImageIcon(selectedFile.getAbsolutePath());
                // Resize image to fit the button size
                Image image = imageIcon.getImage().getScaledInstance(button.getWidth(), button.getHeight(), Image.SCALE_SMOOTH);
                button.setIcon(new ImageIcon(image));
                button.setText(""); // Clear the text from the button
                imagePath = selectedFile.getAbsolutePath(); // Set the imagePath when an image is selected
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error loading image: " + e.getMessage());
            }
        }
    }

