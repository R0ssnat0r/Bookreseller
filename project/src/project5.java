    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String title = jTextField1.getText();
        String address = jTextField2.getText();
        String priceStr = jTextField3.getText(); // Get price from text field

        if (imagePath != null && !title.isEmpty() && !address.isEmpty() && !priceStr.isEmpty()) {
            try {
                // Convert price string to BigDecimal
                double price = Double.parseDouble(priceStr);
                
                // Establish connection to the database
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/mydb?useSSL=false", "root", "");
                String sql = "INSERT INTO stuff (image, title, address, price) VALUES (?, ?, ?, ?)";
                PreparedStatement pst = con.prepareStatement(sql);
                
                // Read image file and convert to byte array
                FileInputStream fis = new FileInputStream(new File(imagePath));
                pst.setBinaryStream(1, fis, (int) new File(imagePath).length());
                pst.setString(2, title);
                pst.setString(3, address);
                pst.setDouble(4, price); // Set price
                
                // Execute the update
                pst.executeUpdate();
                JOptionPane.showMessageDialog(this, "Data saved successfully!");
                con.close();

                // Navigate to project4
                new project4().setVisible(true);
                this.dispose(); // Close project5
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error saving data: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please fill in all fields and select an image.");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

